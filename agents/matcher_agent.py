def match_skills(cv_skills, job_skills):
    cv_set = {s.lower() for s in cv_skills}
    matched, missing, suggestions = [], [], []

    for skill in job_skills:
        skill_lower = skill.lower()
        if skill_lower in cv_set:
            matched.append(skill)
        else:
            missing.append(skill)
            if skill_lower in SUGGESTIONS:
                suggestions.append(SUGGESTIONS[skill_lower])

    matched = list(set(matched))
    missing = list(set(missing))
    suggestions = list(set(suggestions))
    score = round((len(matched) / len(job_skills)) * 100) if job_skills else 0

    return {
        "match_percentage": score,
        "matched_skills": matched,
        "missing_skills": missing,
        "improvement_suggestions": suggestions,
    }


def _detect_domains(cv_set):
    scores = {}
    for domain, signals in DOMAIN_SIGNALS.items():
        overlap = len(cv_set & signals)
        if overlap > 0:
            scores[domain] = overlap
    if not scores:
        return list(DOMAIN_SIGNALS.keys())
    max_score = max(scores.values())
    # include domains within 1 hit of the top (catches adjacent domains e.g. finance + business)
    return [d for d, s in scores.items() if s >= max(1, max_score - 1)]


def recommend_jobs(cv_skills):
    cv_set = {s.lower() for s in cv_skills}
    relevant_domains = _detect_domains(cv_set)
    results = []

    for role in JOB_ROLES:
        if ROLE_DOMAINS.get(role["title"]) not in relevant_domains:
            continue
        matched = cv_set & role["required_skills"]
        score = round((len(matched) / len(role["required_skills"])) * 100)
        missing = role["required_skills"] - cv_set
        suggestions = [SUGGESTIONS[s] for s in missing if s in SUGGESTIONS]
        results.append({
            "title": role["title"],
            "description": role["description"],
            "match_percentage": score,
            "matched_skills": sorted(matched),
            "missing_skills": sorted(missing),
            "suggestions": suggestions,
        })

    results.sort(key=lambda x: x["match_percentage"], reverse=True)

    # fallback: if too few results, open up to all domains
    if len(results) < 3:
        seen = {r["title"] for r in results}
        extras = []
        for role in JOB_ROLES:
            if role["title"] in seen:
                continue
            matched = cv_set & role["required_skills"]
            score = round((len(matched) / len(role["required_skills"])) * 100)
            missing = role["required_skills"] - cv_set
            suggestions = [SUGGESTIONS[s] for s in missing if s in SUGGESTIONS]
            extras.append({
                "title": role["title"],
                "description": role["description"],
                "match_percentage": score,
                "matched_skills": sorted(matched),
                "missing_skills": sorted(missing),
                "suggestions": suggestions,
            })
        extras.sort(key=lambda x: x["match_percentage"], reverse=True)
        results = (results + extras)[:5]

    return results[:5]
